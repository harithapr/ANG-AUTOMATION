package com.example;

public class PregnentWomenMyModel {
    private  String aadharid,address,age,dob,height,husbandname,mobileno,month,name,weight;

    public PregnentWomenMyModel(String aadharid, String address, String age, String dob, String height, String husbandname, String mobileno, String month, String name, String weight) {
        this.aadharid = aadharid;
        this.address = address;
        this.age = age;
        this.dob = dob;
        this.height = height;
        this.husbandname = husbandname;
        this.mobileno = mobileno;
        this.month = month;
        this.name = name;
        this.weight = weight;
    }

    public String getAadharid() {
        return aadharid;
    }

    public void setAadharid(String aadharid) {
        this.aadharid = aadharid;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getDob() {
        return dob;
    }

    public void setDob(String dob) {
        this.dob = dob;
    }

    public String getHeight() {
        return height;
    }

    public void setHeight(String height) {
        this.height = height;
    }

    public String getHusbandname() {
        return husbandname;
    }

    public void setHusbandname(String husbandname) {
        this.husbandname = husbandname;
    }

    public String getMobileno() {
        return mobileno;
    }

    public void setMobileno(String mobileno) {
        this.mobileno = mobileno;
    }

    public String getMonth() {
        return month;
    }

    public void setMonth(String month) {
        this.month = month;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getWeight() {
        return weight;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }
}
